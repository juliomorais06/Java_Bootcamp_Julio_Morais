public class operadorunario {
    public static void main(String[] args) {
        int numero = 5;
        System.out.println(numero);
        System.out.println(- numero); //(operador - nega a expressão)
    }
}
/* (+) Operador únario de valor positivo - números são positivos sem esse
 *     operador explicitamente;
 * (-) Operador unário de valor negativo - nega um número ou expressão
 *     aritmética;
 * (++) Operador unário de incremento de valor - incrementa o valor em
 *      1 unidade;
 * (--) Operador unário de decremento de valor - decrementa o valor em
 *      1 unidade
 * (!) Operador unário lógico de negação - nega o valor de uma 
 *     expessão booleana;
 * 
 */ 